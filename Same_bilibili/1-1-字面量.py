"""
1、掌握字面量的含义
    代码中，被写在代码中固定的值，称之为字面量

2、常见的字面量类型
    代码中常见的6种值（数据）的类型
        数字number
            整数int
                10、-10
            浮点数float
                13.14、-13.14
            复数complex
                4+3j，以j结尾表示复数
            布尔bool
                True表示真，False表示假
                True本质上是一个数字记作1，False记作0
        字符串string
            描述文本的一种数据类型
                由任意数量的字符串组成
        列表list
            有序的可变序列
                python中使用最频繁的数据类型，可有序记录一堆数据
        元组tuple
            有序的不可变序列
                可有序记录一堆不可变的python数据集合
        集合set
            无序不重复集合
                可无序记录一堆不重复的python数据集合
        字典dictionary
            无序key-value集合
                可无序记录一堆key-value型的python数据集合
    
3、如何基于print语句完成各类字面量的输出
"""

"print(字面量)"
"输出整数10"
print(10)
"输出浮点数13.14"
print(13.14)
"输出字符串520"
print("520")
"输出字符串python从入门到放弃"
print("python从入门到放弃")
"输出字符串hello wold"
print("hello world")